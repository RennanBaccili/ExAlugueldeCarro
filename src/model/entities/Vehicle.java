package model.entities;

public class Vehicle {
	private String vehicle;

	
	
	public Vehicle() {
		super();
	}


	public Vehicle(String vehicle) {
		super();
		this.vehicle = vehicle;
	}


	public String getVehicle () {
		return vehicle;
	}


	public void setVehicle(String vehicle ) {
		this.vehicle = vehicle;
	}
	
}
